#include <stdlib.h>

void	*ft_calloc(size_t count, size_t size)
{
	int	i;
	int	ptr;

	ptr = malloc(count * size);
	if (ptr == NULL)
		return (NULL);
	i = 0;
	while (i < count * size)
	{
		ptr[i] = 0;
		i++;
	}
	return (ptr);
}
